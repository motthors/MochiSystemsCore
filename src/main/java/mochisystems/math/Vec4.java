public class Vec4{

    double x;
    double y;
    double z;
    double w;

    public Vec4()
    {
        x=0;
        y=0;
        z=0;
        w=0;
    }
    public Vec4(double x,double y,double z,double w)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }
}